# Cytat z wypowiedzi prezesa IBM, Thomasa Watsona, z 1943 r.

quote = "Myślę, że istnieje świadomy rynek dla może pięciu komputerów."

print("Orginalny cytat w tłumaczeniu na  język polski:")
print(quote)

print("\nDużymi literami:")
print(quote.upper())

print("\nMałymi literami:")
print(quote.lower())

print("\nWszystkie wyrazy od dużych liter:")
print(quote.title())

print("\nZ drobną zmianą:")
print(quote.replace("pięciu", "milionów"))

print("\nOrginalny cytat pozostaje bez zmian:")
print(quote)

